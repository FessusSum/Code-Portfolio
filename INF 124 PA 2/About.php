<h2><b>About Project MOBIUS</b></h2>
<p>
War has changed. It is no longer about lines of infantry clashing against each other on a desolate battlefield. It's an endless series of skirmishes, each side losing little by little instead of entire battalions at a time. War, and its consumption of life, has become a well-oiled machine. Soldiers fight and soldiers die, only to return the next day to repeat the process all over again. The only thing that can break this bloody cycle is air power. Thunderous strikes raining from the sky can scatter entire armies. What was once an endless quagmire soon turns into a decisive victory.
<br>
<br>
But for every winner, there must be a loser. As aeronautical technology grows, the power gap between the superpowers of the world and everyone else grows more and more. Soon, there will only be the strongest nations remaining, each vying for control and power once more and continuing the cycle of war.
<br>
<br>
Project MOBIUS aims to correct this power imbalance. By providing all nations of the world with the tools to succeed in war, an equilibrium is created. No longer will lives be so carelessly thrown away when defeat is a possibility for everyone. Soon, within this steel equilibrium, these shattered skies will become whole once more.
</p>
<br>
<h3>Mission Statement</h3>
We at Project MOBIUS strive to uphold the equilibrium that our founders envisioned for the world, working ceaselessly to provide aircraft of the highest quality to nations in need of an air force. From ground attack to air superiority, Project MOBIUS caters to every need from any nation. One day, we will be able to fly with our brothers and sisters under the same sky not as enemies, but as comrades. 

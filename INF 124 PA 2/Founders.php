<h3>Mobius 1</h3>

<p>Also known as the "Ribbon Fighter" for the Mobius strip adorning his signature F-22, Mobius 1 is an enigma. Despite his legendary combat feats, almost nothing is known about him. He spent most of his military career without a wingman, defeating scores of enemy pilots while hopelessly outnumbered time and time again. It is said that Mobius One alone could turn the tide of a war. Oddly enough, Mobius One would go on to suppressing an entire armed insurrection accompanied only by a single AWACS unit, proving the rumors correct once and for all. After single handedly defeating an insurrectionist group, Mobius One quietly retired from the armed forces and disappeared from public eye. Project MOBIUS marks his first appearance in several years. Unlike the other founders, Mobius 1 seems to work out of respect for the art of flying moreso than any political agenda. His piloting skills are matched only by his ceaseless dedication to making sure that every plane on offer by Project MOBIUS is of the highest quality. </p>

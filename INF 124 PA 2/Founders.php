<h2><b>Founders</b></h2>
Project MOBIUS was originally founded by three legendary aces of varying nationality. Disgusted with the way that soldiers were carelessly discarded over games of politics between world superpowers, they set out to break the tyrannical hold these nations had over the rest of the world.

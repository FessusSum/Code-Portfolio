<h3>Cipher</h3>
<p>Also known as Galm 1, much of Cipher's life was spent as a mercenary. With his wingman Larry "Pixy" Foulke, Cipher carved a bloody path through multiple wars. His career as a mercenary soon came to a crashing halt during an engagement against the nation of [REDACTED], when guerilla fighters detonated several repurposed "Mother of all Bombs" on their own soil in an attempt to stall the advancing mercenaries. Already suffering from PTSD due to multiple recent engagements with [REDACTED] armed forces, "Pixy" turned on Cipher and attempted to kill him an an apparent murder/suicide attempt, citing the "madness that took hold of both nations". After damaging Pixy's left engine, Cipher was forced to watch his friend die as Pixy refused to eject from his doomed aircraft. Shamed, Cipher soon left his mercenary group and sought to find an end to the madness that is war, leading him to Project MOBIUS.   



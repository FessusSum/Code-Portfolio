<h3>Blaze</h3>
<p>Also known as Razgriz 1, Blaze's military career was a tumultuous one. Originally a rookie pilot in the [REDACTED] Air Force, Blaze's squadron was one of many sent as a counteroffensive against [REDACTED]. Unfortunately, his nation's warmongering politicians conspired to prolong the war in an attempt to boost [REDACTED]'s flagging economy. As Blaze's squadron returned from victory after victory, his nation's leaders plotted to frame them for treason and execute them in order to prevent them from bring the war to an end. While Blaze and his fellow pilots eventually escaped the clutches of his own government, the callousness his leaders showed towards the lives of his fellow soldiers left a deep impression on him. He soon set out to find other aces to help him make sure that soldiers would never again be tossed away so cheaply, eventually founding Project MOBIUS. </p>
